public class SwitchCase1 {

    public static void main(String[] args) {

        int number = 40;

        switch (number){
            case 1:
                System.out.println("10");
                break; //Thoat khoi vong switch
            case 2:
                System.out.println("20");
                break;
            case 3:
                System.out.println("30");
                break;
            default:
                System.out.println("Not in 10, 20 or 30");
        }

    }

}
